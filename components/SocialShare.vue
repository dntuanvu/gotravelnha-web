<script setup lang="ts">
import { ref, computed } from 'vue'
import { useRoute } from 'vue-router'
import { useActivityTracker } from '~/composables/useActivityTracker'

const props = defineProps<{
  title?: string
  description?: string
  url?: string
  image?: string
  platform?: string
  dealId?: string
}>()

const route = useRoute()
const { trackClick } = useActivityTracker()

const copied = ref(false)

const shareUrl = computed(() => props.url || (typeof window !== 'undefined' ? window.location.href : ''))
const shareTitle = computed(() => props.title || 'Check out this travel deal on GoVietHub!')
const shareDescription = computed(() => props.description || 'Compare and book the best travel deals')
const shareImage = computed(() => props.image || 'https://storage.googleapis.com/travella_assets_images/app_icon.png')

const shareOnFacebook = () => {
  const url = `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(shareUrl.value)}`
  window.open(url, '_blank', 'width=600,height=400')
  trackClick('social_share', { platform: 'facebook', url: shareUrl.value, dealId: props.dealId })
}

const shareOnTwitter = () => {
  const text = `${shareTitle.value} ${shareUrl.value}`
  const url = `https://twitter.com/intent/tweet?text=${encodeURIComponent(text)}`
  window.open(url, '_blank', 'width=600,height=400')
  trackClick('social_share', { platform: 'twitter', url: shareUrl.value, dealId: props.dealId })
}

const shareOnWhatsApp = () => {
  const text = `${shareTitle.value}\n${shareDescription.value}\n${shareUrl.value}`
  const url = `https://wa.me/?text=${encodeURIComponent(text)}`
  window.open(url, '_blank')
  trackClick('social_share', { platform: 'whatsapp', url: shareUrl.value, dealId: props.dealId })
}

const shareOnTelegram = () => {
  const text = `${shareTitle.value}\n${shareDescription.value}\n${shareUrl.value}`
  const url = `https://t.me/share/url?url=${encodeURIComponent(shareUrl.value)}&text=${encodeURIComponent(text)}`
  window.open(url, '_blank')
  trackClick('social_share', { platform: 'telegram', url: shareUrl.value, dealId: props.dealId })
}

const copyLink = async () => {
  try {
    await navigator.clipboard.writeText(shareUrl.value)
    copied.value = true
    trackClick('social_share', { platform: 'copy', url: shareUrl.value, dealId: props.dealId })
    setTimeout(() => { copied.value = false }, 2000)
  } catch (error) {
    console.error('Failed to copy link:', error)
  }
}
</script>

